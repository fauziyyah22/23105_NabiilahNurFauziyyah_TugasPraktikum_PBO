    //Metode untuk memperbarui tampilan tabel dengan data mahasiswa
    private void updateTable() {
        tableModel.setRowCount(0); //Hapus baris yang ada
        for (Mahasiswa mhs : listMahasiswa) {
            Object[] row = {mhs.getNIM(), mhs.getNama(), mhs.getAlamat(), mhs.getMataKuliah(), String.format("%.2f", mhs.getNilaiAkhir())};
            tableModel.addRow(row);
        }
    }


    //Metode untuk menghitung nilai akhir berdasarkan input nilai
    private void hitungNilaiAkhir() {
        try {
            //Mengambil nilai dari field teks dan mengonversi ke tipe double
            double nilai1 = Double.parseDouble(nilai1TF.getText());
            double nilai2 = Double.parseDouble(nilai2TF.getText());
            double nilai3 = Double.parseDouble(nilai3TF.getText());
            double nilai4 = Double.parseDouble(nilai4TF.getText());
            double nilai5 = Double.parseDouble(nilai5TF.getText());

            //Hitung nilai akhir sesuai bobot
            double nilaiAkhir = (nilai1 * 0.10) + (nilai2 * 0.15) + (nilai3 * 0.25) + (nilai4 * 0.15) + (nilai5 * 0.35);
            nilaiAkhirTF.setText(String.valueOf(nilaiAkhir));
        } catch (NumberFormatException ex) {
            nilaiAkhirTF.setText(""); // Kosongkan jika ada error format angka
        }
    }    
    
    //Buttton Simpan
    private void simpanbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanbtnActionPerformed
        // TODO add your handling code here:
        String nim = nimTF.getText();
        String nama = namaTF.getText();
        String alamat = alamatTF.getText();
        String mataKuliah = mataKuliahTF.getText();

        hitungNilaiAkhir();
                
        double nilaiAkhir;
        try {
            nilaiAkhir = Double.parseDouble(nilaiAkhirTF.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nilai akhir tidak valid.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Hentikan eksekusi jika nilai akhir tidak valid
        }

        // Periksa apakah ada field yang kosong
        if (nim.isEmpty() || nama.isEmpty() || alamat.isEmpty() || mataKuliah.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pastikan semua nilai telah diisi dengan benar.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Hentikan eksekusi jika ada field yang kosong
        }
        
        //Buat objek mahasiswa baru dan tambahkan ke list
        Mahasiswa mhs = new Mahasiswa(nim, nama, alamat, mataKuliah, nilaiAkhir);
        listMahasiswa.add(mhs);
        
        // Update tabel
        updateTable();
        // Bersihkan field setelah menyimpan
        clearFields();
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan.", "Info", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_simpanbtnActionPerformed
    
    //Button Hapus
    private void hapusbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusbtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = mahasiswaTB.getSelectedRow();//Ambil baris yang dipilih di tabel
        if (selectedRow != -1) {
            listMahasiswa.remove(selectedRow);//Hapus mahasiswa dari list
            updateTable();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.", "Info", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_hapusbtnActionPerformed

